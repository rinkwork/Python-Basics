#Addition
print(f'3 + 5 = {3 + 5}')

#subtraction
print(f'7 - 4 = {7 - 4}')

#multiplications
print(f'2 * 8 = {2 * 8}')

#division
print(f'10 / 5 = {10 / 5}')

#floor division
print(f'12 // 5 = {12 // 5}')

#power or exponentiation
print(f'2 ^ 3 = {2 ** 3}')

#modulus
print(f'6 % 3 = {6 % 3}')
print(f'7 % 3 = {7 % 3}')